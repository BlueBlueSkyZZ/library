package po;

public class PayList {
	private String weid;
	private String subsribenum;
	private String money; 
	private String bookno;
	private String bookname;
	private String status;
	private String whetherPay;
	
	public String getWeid() {
		return weid;
	}
	public void setWeid(String weid) {
		this.weid = weid;
	}
	public String getSubsribenum() {
		return subsribenum;
	}
	public void setSubsribenum(String subsribenum) {
		this.subsribenum = subsribenum;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getWhetherPay() {
		return whetherPay;
	}
	public void setWhetherPay(String whetherPay) {
		this.whetherPay = whetherPay;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
	public String getBookno() {
		return bookno;
	}
	public void setBookno(String bookno) {
		this.bookno = bookno;
	}
	public String getBookname() {
		return bookname;
	}
	public void setBookname(String bookname) {
		this.bookname = bookname;
	}
	
}
